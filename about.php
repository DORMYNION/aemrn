						<h3>Vision of the Afro-European Medical and Research Network (AEMRN)</h3>
						<br>
						<p>The Network now in Special Consultative Status with the <a href="http://www.un.org/en/ecosoc/">United Nations Economic and Social Council (ECOSOC)</a> is Non-Governmental and non-profit Organisation with a strong vision of helping to improve the quality of life for people from and living in resource limited settings. It has Bern Switzerland as overall and European headquarters and Liberia as Africa Regional headquarters. Projects in health, food security etc. have been initiated in various resource-limited countries especially Sub-Saharan Africa.</p>
						<br>
						<p>Its members strive to contribute to the medical, mental, social, educational and every sound well-being of mankind irrespective of race, creed, beliefs, faith and social affiliation.</p>
						<br>
						<p>AEMRN serves as a platform wherein active professionals from such disciplines as education, medicine, engineering, nursing, faith-based can interact at various levels of self-initiated conferences, seminars, workshops, exchange programmes and voluntary workcamps.</p>
						<br>
						<p>We endeavour to share ideas and knowledge for the betterment of the less fortunate human sectors of the world mainly from and living in poor-resource settings. As a team, we synergize our efforts to maximize our output and reach out to needy communities. AEMRN team offer them our services whilst at the same time forstering and promoting cross-cultural exchange and Global Peace both within our own multinational and multi-professional team as well as the people we will be interacting with to help.</p>
						<br>
						<p> The members of AEMRN are aware of the fact that there are people with very good learnt trade and profession living under diverse conditions in different parts of the world who, due to one reason or another, are unable to put into useful purpose both for themselves and their communities. AEMRN members identify, reach out to such people, encourage and empower them to take part in continuous professional development programmes so that the stimulus of their learnt trade can be rekindled, improved and once again contribute to the development of their community and the world at large.</p>
						<br>
						<p>We collaborate with human peace-loving organisations in diverse projects geared towards betterment of the poor people and enhancing their capacity building.</p>
						<br>
						<p>We provide liason between international health organisations both in the West and the African counterparts so that as a combined group we can at various levels augment one another's effort to fight Communicable diseases especially HIV/AIDS as well as non-communicable ones.</p>
						<br>
						<p>The necessary bridge between health, education and environment has urged our communication and language department to develop beside interpreters training, conflict resolution and mediation know-how as well as tourist-guiding facilities. This ambitious vision is in response to multifaceted challenges awaiting Diaspora Africans and our team. As a contribution to the future of the world, we have set ourselves the inescapable duty to forge, the dedicated mission to nurture and the long lasting will to further our goals.</p>
